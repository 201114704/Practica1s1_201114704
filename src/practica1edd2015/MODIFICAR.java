        boolean TipoAtaque;
        if(jTextField4.getText().equals("directo")){
                TipoAtaque=false;
        }else{
                TipoAtaque=true;
        }
        if(jLabel1.getText().equals("PLANTA")){

            CONFIGURACION_PLANTAS.Lista_Plantas.MODIFICAR(NombreOriginalRecibido_Nodo,jTextField1.getText(),Integer.parseInt(jTextField2.getText()),Integer.parseInt(jTextField3.getText()),TipoAtaque);
            this.dispose();
        }
        else if(jLabel1.getText().equals("ZOMBIE")){
            CONFIGURACION_ZOMBIES.Lista_Zombies.MODIFICAR(NombreOriginalRecibido_Nodo,jTextField1.getText(),Integer.parseInt(jTextField2.getText()),Integer.parseInt(jTextField3.getText()),TipoAtaque);
            this.dispose();
        }
